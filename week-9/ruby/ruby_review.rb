def is_fibonacci?(num)

  
  if Math.sqrt(5*num**2 + 4)==Math.sqrt(5*num**2 + 4).floor
    return true
  end
    
  if Math.sqrt(5*num**2 - 4)==Math.sqrt(5*num**2 -4).floor
    return true
  end
  
  return false
end

